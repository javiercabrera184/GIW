    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        
        Buscador b= new Buscador();
        try {
            if(!"".equals(jTextField1.getText())){
                docs=b.buscar(jTextField1.getText(),jComboBox1.getSelectedItem().toString());
            }
        } catch (IOException | ParseException | java.text.ParseException ex) {
            Logger.getLogger(Interfaz1.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextField2.setText(Integer.toString(docs.size()));
        DefaultListModel<String> model = new DefaultListModel<>();
       

        for ( int i = 0; i < docs.size(); i++ ){
          model.addElement( docs.get(i).getExtracto() );
        }
        
        jList1.setModel(model);
        jList1.addListSelectionListener((ListSelectionEvent arg0) -> {
            if (!arg0.getValueIsAdjusting()) {
                //jTextArea1.setText(jList1.getSelectedValue().toString());
                for ( int i = 0; i < docs.size(); i++ ){
                    if(jList1.getSelectedValue() == null ? docs.get(i).getExtracto() == null : jList1.getSelectedValue().equals(docs.get(i).getExtracto())){
                        String output="";
                        String [] s = null;
                        try {
                            
                            s=b.resaltar(jTextField1.getText(),jComboBox1.getSelectedItem().toString(),docs.get(i).mostrar());
                        } catch (ParseException | IOException | InvalidTokenOffsetsException ex) {
                            Logger.getLogger(Interfaz1.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        for(int j=0;j<s.length;j++){
                            //System.out.println("Palabras resaltadas: "+s[j]);
                            
                                output+=s[j];
                            
                        }
                        
                        if(s.length==0){
                            String fin=escape(docs.get(i).mostrar());
                            jEditorPane1.setText(fin);
                        }else{
                            String fin=escape(output);
                            jEditorPane1.setText(fin);
                        }
                        
                    }
                }
            }
        });
    }//GEN-LAST:event_jButton1ActionPerformed
    public static String escape(String s) {
        StringBuilder builder = new StringBuilder();
        boolean previousWasASpace = false;
        for( char c : s.toCharArray() ) {
            if( c == ' ' ) {
                if( previousWasASpace ) {
                    builder.append("&nbsp;");
                    previousWasASpace = false;
                    continue;
                }
                previousWasASpace = true;
            } else {
                previousWasASpace = false;
            }
            switch(c) {
                //case '<': builder.append("&lt;"); break;
                //case '>': builder.append("&gt;"); break;
                case '&': builder.append("&amp;"); break;
                case '"': builder.append("&quot;"); break;
                case '\n': builder.append("<br>"); break;
                // We need Tab support here, because we print StackTraces as HTML
                case '\t': builder.append("&nbsp; &nbsp; &nbsp;"); break;  
                default:
                    if( c < 128 ) {
                        builder.append(c);
                    } else {
                        builder.append("&#").append((int)c).append(";");
                    }    
            }
        }
        return builder.toString();
    }
    
